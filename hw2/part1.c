#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>
#include <string.h>
 
int main(void) {
  int i = 0;
  int sum = 0;
  double avg = 0;
  int num = 0;
  while (scanf("%d", &i) > 0) {
    sum += i;
    num++;
    printf("num is: %d\n", num);
  }
  avg = sum / num;
  printf("avg is: %f\n", avg);     
}
